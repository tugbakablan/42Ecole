int	ft_sqrt(int nb)
{
	int	x;

	x = 2;
	if (nb == 1)
		return (1);
	while (x * x <= nb && x < 46341)
	{
		if (x * x == nb)
			return (x);
		x++;
	}
	return (0);
}
