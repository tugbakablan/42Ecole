int	ft_str_is_alpha(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 1;
	while (str[i] != '\0')
	{
		if ((str[i] > 'Z' || str[i] < 'A') && (str[i] < 'a' || str[i] > 'z'))
		{
			x = 0;
		}
		i++;
	}
	return (x);
}
