int	ft_str_is_numeric(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 1;
	while (str[i] != '\0')
	{
		if (str[i] > '9' || str[i] < '0')
		{
			x = 0;
		}
		i++;
	}
	return (x);
}
