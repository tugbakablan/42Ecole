int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 1;
	while (str[i] != '\0')
	{
		if (str[i] > 'z' || str[i] < 'a')
		{
			x = 0;
		}
		i++;
	}
	return (x);
}
